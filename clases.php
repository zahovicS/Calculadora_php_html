<?php 

	class Matematica{

		public static function Sumar($n1,$n2)
		{
			$suma=$n1+$n2;
			return $suma;
		}

		public static function Resta($n1,$n2)
		{
			$suma=$n1-$n2;
			return $suma;
		}

		public static function Multiplicacion($n1,$n2)
		{
			$suma=$n1*$n2;
			return $suma;
		}

		public static function Division($n1,$n2)
		{
			$suma=$n1/$n2;
			return $suma;
		}
	}

?>